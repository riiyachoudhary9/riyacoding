
// 2. Operators example: arithmetic, relational, logical
#include <stdio.h>
int main() {
    int x = 10, y = 3;
    int sum = x + y;
    int rel = (x > y);
    int logic = (x > 5 && y < 5);

    printf("Sum: %d\n", sum);
    printf("x > y: %d\n", rel);
    printf("Logical AND result: %d\n", logic);
    return 0;
}


